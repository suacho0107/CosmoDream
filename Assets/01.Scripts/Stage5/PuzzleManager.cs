            puzzle.SetPuzzleManager(this); // ?? ???? ?????? PuzzleManager?? ????
        }
    }

    public void CheckPuzzleCompletion()
    {
        foreach (var puzzle in puzzles)
        {
            if (!puzzle.IsLocked()) // ???? ?????? ???????? ???? ????
            {
                return; // ?????? ???? ???????? ????
            }
        }

        Debug.Log("???? ????!"); // ???? ???? ?????? ?????? ????
        if(sceneName == "0")
        {
            puzzleClear.CompletePuzzle();
        }
        else SceneManager.LoadScene(sceneName);
    }
}
